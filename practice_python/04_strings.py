# Strings: ordered, immutable, text rep

my_string = "Hello World"
substring = my_string[::-1]
print(substring)

greeting = "Hello"
name = "Tom"
sentence = greeting + " " + name
print(sentence)

my_string = 'how are you doing?'
mylist = my_string.split()
print(mylist)
new_str = " ".join(mylist)
print(new_str)

my_lst = ['a'] * 6
my_str = ''.join(my_lst)
print(my_str)

var = "Tom"
mystr = f'the variable is {var}'
print(mystr)



